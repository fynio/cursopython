def Tipo_de_dato(dato):
    return print(type(dato));

# str
x  = "HELLLO WORLD"
Tipo_de_dato(x)

# int
x  = 20
Tipo_de_dato(x)


# float
x  = 20.5
Tipo_de_dato(x)




print("----------------------")
# complex
x  = 1j
Tipo_de_dato(x)



# tuple
x = ("apple", "banana", "cherry")
 


# range
x = range(6)
Tipo_de_dato(x)


# dict
x = {"name" : "John", "age" : 36}
Tipo_de_dato(x)



# set
x = {"apple", "banana", "cherry"}
Tipo_de_dato(x)



# frozenset
x = frozenset({"apple", "banana", "cherry"})
Tipo_de_dato(x)

# boolean
x = True
Tipo_de_dato(x)



# bytes
x = b"Hello"
Tipo_de_dato(x)

# bytearray
x = bytearray(5)
Tipo_de_dato(x)


# memoryview
x = memoryview(bytes(5))
Tipo_de_dato(x)
